char	*ft_strlowcase(char *str)
{
	char	*head;

	head = str;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
			*str = *str - 'A' + 'a';
		str++;
	}
	return (head);
}
